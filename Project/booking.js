 var opacity = 0;
 var intervalID = 0;
 window.onload = fadeIn;

 function fadeIn() {
     setInterval(show, 400);
 }

 function show() {
     var maindiv = document.getElementById("maindiv");
     opacity = Number(window.getComputedStyle(maindiv)
                     .getPropertyValue("opacity"));
     if (opacity < 1) {
         opacity = opacity + 0.1;
         maindiv.style.opacity = opacity
     } else {
         clearInterval(intervalID);
     }
 }


